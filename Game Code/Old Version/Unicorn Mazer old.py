# FUNÇÃO PARA DEIXAR O SCORE SEMPRE COM 6 DIGITOS
def num6dig(num):
    l = ['0','0','0','0','0','0']
    index = 6
    while (num !=0):
        l.insert(index, str(num%10))
        num = num // 10
        index -= 1
    
        l.pop(0)
    return ''.join(l)


